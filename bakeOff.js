const ingredientCheck = function(bakeryArr, recipe) {
  return bakeryArr.some(value => recipe.includes(value));
};

const chooseRecipe = function(bakeryA, bakeryB, recipes) {
  for (let recipe of recipes) {
    if (ingredientCheck(bakeryA, recipe.ingredients)) {
      if (ingredientCheck(bakeryB, recipe.ingredients)) {
        return recipe.name;
      }
    }
  }
  return `No matching ingredients`;
};

let bakeryA = ['saffron', 'eggs', 'tomato paste', 'coconut', 'custard'];
let bakeryB = ['milk', 'butter', 'cream cheese'];
let recipes = [
  {
    name: 'Coconut Sponge Cake',
    ingredients: ['coconut', 'cake base']
  },
  {
    name: 'Persian Cheesecake',
    ingredients: ['saffron', 'cream cheese']
  },
  {
    name: 'Custard Surprise',
    ingredients: ['custard', 'ground beef']
  }
];

console.log(chooseRecipe(bakeryA, bakeryB, recipes));

